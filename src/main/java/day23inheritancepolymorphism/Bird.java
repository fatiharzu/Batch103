/**
 * <h2>"final" keyword nedir?</h2>
 * <lu>
 *     <li>Variable'larle kullanilabilir</li>
 *     <p>
 *         public final int age = 12;<br>
 *         a) Atanan deger degistirilemez
 *         b) Deger atamasi yapmak zorundasiniz.
 *     </p>
 *     <li>Method'larda kullanilabilir</li>
 *     <p>
 *         public final int add(){
 *             return a+b
 *         }
 *         a) Bir method olusturulurken final olarak olusturulmus ise o methodun body si asla degistirilemez.
 *           Dolayisiyla o method override edilemez
 *     </p>
 *     <li>Class'larda kullanilabilir</li>
 *     <p>
 *         Bir clas i final yaparsaniz o Class i kisirlastirmis olursunuz
 *         Bir class fnal ise o Class a extend edilemez
 *         "final" Class Child olabilir.
 *     </p>
 * </lu>
 */

